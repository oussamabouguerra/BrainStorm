package Entities;

/**
 *
 * @author mouad
 */
public class SimpleUser extends User{
    private int idUser;

    public SimpleUser() {
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    @Override
    public String toString() {
        return "SimpleUser{" + "idUser=" + idUser + '}';
    }
    
}
